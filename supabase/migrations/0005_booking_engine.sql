-- ============================================================
-- 1. ENUMS
-- ============================================================

-- Full booking state machine per Revenue doc §3.1 and Blueprint §0.1.
-- Terminal states: all cancelled_*, no_show.
-- Quasi-terminal: completed (can move to disputed); disputed can
-- resolve back to completed or to cancelled_by_system.
CREATE TYPE public.app_booking_status AS ENUM (
  'pending_payment',       -- booking created; availability held; awaiting payment
  'payment_processing',    -- payment attempt in flight at provider
  'awaiting_host',         -- payment held; host has 24h to accept (Request-to-Book)
  'confirmed',             -- payment captured; host accepted (or Instant Book)
  'checked_in',            -- arrival recorded; stay in progress
  'completed',             -- stay ended 24h+ ago; no active dispute
  'cancelled_by_traveler', -- traveler cancelled; policy-based refund
  'cancelled_by_host',     -- host cancelled; 100% refund + host strike
  'cancelled_by_system',   -- timeout, payment failure, or platform action
  'no_show',               -- host declared no-show within 24h of check_in
  'disputed'               -- dispute opened ≤72h after completed
);

CREATE TYPE public.app_payout_status AS ENUM (
  'pending',    -- booking completed; payout not yet batched
  'scheduled',  -- included in a payout batch
  'paid',       -- disbursed to host
  'on_hold',    -- frozen by dispute or KYC issue
  'reversed'    -- clawback applied after chargeback or fraud
);

-- Named event types for the append-only booking_events audit trail.
CREATE TYPE public.app_booking_event_type AS ENUM (
  'booking_created',
  'payment_initiated',
  'payment_succeeded',
  'payment_failed',
  'awaiting_host',
  'host_accepted',
  'host_declined',
  'booking_confirmed',
  'checked_in',
  'completed',
  'cancelled_by_traveler',
  'cancelled_by_host',
  'cancelled_by_system',
  'no_show_declared',
  'dispute_opened',
  'dispute_resolved_host',
  'dispute_resolved_traveler',
  'payout_scheduled',
  'payout_paid',
  'refund_initiated',
  'refund_completed'
);


-- ============================================================
-- 2. SECURITY-DEFINER HELPER: is_traveler_of_booking
-- ============================================================
-- Used by booking_guests and booking_events RLS SELECT policies
-- to avoid subqueries that would re-evaluate RLS on bookings
-- recursively.  Follows the same hardening pattern as has_role()
-- and is_host_of().

CREATE OR REPLACE FUNCTION public.is_traveler_of_booking(
  _user_id    uuid,
  _booking_id uuid
)
  RETURNS boolean
  LANGUAGE sql
  SECURITY DEFINER
  STABLE
  PARALLEL SAFE
  SET search_path = ''
AS $$
  SELECT CASE
    WHEN _user_id IS NULL OR _booking_id IS NULL THEN false
    ELSE EXISTS (
      SELECT 1
      FROM public.bookings b
      WHERE b.id          = _booking_id
        AND b.traveler_id = _user_id
    )
  END;
$$;

GRANT EXECUTE ON FUNCTION public.is_traveler_of_booking(uuid, uuid)
  TO authenticated;


-- ============================================================
-- 3. BOOKINGS
-- ============================================================
-- Central transaction record.  Financial columns (accommodation_amount
-- through host_payout_amount) are frozen at transition to 'confirmed'
-- and must never be updated afterwards.  The CHECK constraints below
-- encode the revenue model invariants from Revenue doc §1.2–1.3 at
-- the database layer so no code path can produce a mathematically
-- inconsistent booking.

CREATE TABLE IF NOT EXISTS public.bookings (
  id          uuid                    PRIMARY KEY DEFAULT extensions.gen_random_uuid(),

  -- Human-readable reference shown to both parties: STBF-XXXXXX
  -- Generated by the server function using Crockford base32.
  reference   text                    NOT NULL,

  traveler_id uuid                    NOT NULL,
  property_id uuid                    NOT NULL,
  room_id     uuid                    NOT NULL,

  -- Date range (check_out is the departure day — NOT a booked night)
  check_in    date                    NOT NULL,
  check_out   date                    NOT NULL,

  -- Computed and stored so reports can filter without recalculation
  nights      integer                 GENERATED ALWAYS AS (check_out - check_in) STORED,

  guests_adults   integer             NOT NULL DEFAULT 1,
  guests_children integer             NOT NULL DEFAULT 0,
  guests_infants  integer             NOT NULL DEFAULT 0,

  status      public.app_booking_status NOT NULL DEFAULT 'pending_payment',

  -- Snapshot of room.instant_book at booking creation time; determines
  -- which branch of the state machine is taken after payment.
  instant_book boolean                NOT NULL DEFAULT false,

  -- ── Financial snapshot ─────────────────────────────────────────────
  -- All amounts in integer FCFA.  Frozen at transition to 'confirmed'.
  -- service_fee_rate is always 0.1000 (Revenue doc §1.2).
  -- commission_rate is 0.1500 (free plan) or 0.0000 (paid plan),
  -- determined by host's active subscription at confirmation time
  -- (Revenue doc §1.3 snapshot rule).

  accommodation_amount    integer         NOT NULL,
  service_fee_rate        numeric(5,4)    NOT NULL DEFAULT 0.1000,
  service_fee_amount      integer         NOT NULL,
  commission_rate         numeric(5,4)    NOT NULL,
  commission_amount       integer         NOT NULL,
  total_amount            integer         NOT NULL,
  host_payout_amount      integer         NOT NULL,
  currency                text            NOT NULL DEFAULT 'XOF',

  -- Frozen subscription state used by payout and analytics.
  -- Shape: {plan_code, plan_id, period_end, commission_rate}
  host_subscription_snapshot jsonb        NOT NULL DEFAULT '{}'::jsonb,

  -- ── Cancellation ───────────────────────────────────────────────────
  -- Snapshot of property.cancellation_policy at booking creation time.
  cancellation_policy   public.app_cancellation_policy NOT NULL,
  cancellation_reason   text,
  cancelled_at          timestamptz,
  cancelled_by          uuid,           -- FK → profiles(id) ON DELETE SET NULL

  -- ── Payout tracking ────────────────────────────────────────────────
  payout_status public.app_payout_status NOT NULL DEFAULT 'pending',
  payout_id     uuid,                   -- FK → payouts(id) — set in Migration 0006

  -- ── Lifecycle timestamps ───────────────────────────────────────────
  confirmed_at  timestamptz,
  checked_in_at timestamptz,
  completed_at  timestamptz,
  created_at    timestamptz             NOT NULL DEFAULT now(),
  updated_at    timestamptz             NOT NULL DEFAULT now(),

  -- ── Constraints ────────────────────────────────────────────────────

  CONSTRAINT bookings_reference_unique UNIQUE (reference),

  CONSTRAINT bookings_dates_valid CHECK (check_out > check_in),
  CONSTRAINT bookings_nights_pos  CHECK ((check_out - check_in) > 0),

  CONSTRAINT bookings_guests_adults_pos    CHECK (guests_adults >= 1),
  CONSTRAINT bookings_guests_children_pos  CHECK (guests_children >= 0),
  CONSTRAINT bookings_guests_infants_pos   CHECK (guests_infants >= 0),

  -- Revenue model invariants (Revenue doc §1.2–1.3 + dev notes §2)
  CONSTRAINT bookings_service_fee_rate     CHECK (service_fee_rate = 0.1000),
  CONSTRAINT bookings_commission_rate_valid CHECK (
    commission_rate IN (0.0000, 0.1500)
  ),
  CONSTRAINT bookings_accommodation_pos    CHECK (accommodation_amount > 0),
  CONSTRAINT bookings_total_invariant      CHECK (
    total_amount = accommodation_amount + service_fee_amount
  ),
  CONSTRAINT bookings_payout_invariant     CHECK (
    host_payout_amount = accommodation_amount - commission_amount
  ),
  CONSTRAINT bookings_currency_xof         CHECK (currency = 'XOF'),

  -- Lifecycle timestamp consistency
  CONSTRAINT bookings_confirmed_at_consistency CHECK (
    (status = 'confirmed' AND confirmed_at IS NOT NULL)
    OR status != 'confirmed'
  ),
  CONSTRAINT bookings_completed_at_consistency CHECK (
    (status = 'completed' AND completed_at IS NOT NULL)
    OR status != 'completed'
  ),

  -- ── Foreign keys ───────────────────────────────────────────────────
  FOREIGN KEY (traveler_id)   REFERENCES public.profiles (id)     ON DELETE RESTRICT,
  FOREIGN KEY (property_id)   REFERENCES public.properties (id)   ON DELETE RESTRICT,
  FOREIGN KEY (room_id)       REFERENCES public.rooms (id)        ON DELETE RESTRICT,
  FOREIGN KEY (cancelled_by)  REFERENCES public.profiles (id)     ON DELETE SET NULL
  -- payout_id FK added in Migration 0006 after payouts table is created
);


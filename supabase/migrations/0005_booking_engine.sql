-- ============================================================
-- 8. TRIGGER: expire_pending_bookings (cron support)
-- ============================================================
-- Called by the nightly/5-minute cron job via service_role.
-- Releases availability and cancels bookings that have been in
-- 'pending_payment' for more than 15 minutes without a payment
-- being initiated.
--
-- Concurrency guard: the UPDATE on bookings uses
-- AND status = 'pending_payment' so that a booking that was
-- advanced to 'payment_processing' by a concurrent webhook between
-- the SELECT and the UPDATE is left untouched.
--
-- This function is NOT called on every row change — it is invoked
-- explicitly by the cron server function via:
--   SELECT public.expire_pending_bookings();

CREATE OR REPLACE FUNCTION public.expire_pending_bookings()
  RETURNS integer        -- number of bookings expired in this run
  LANGUAGE plpgsql
  SECURITY DEFINER
  SET search_path = ''
AS $$
DECLARE
  v_booking   record;
  v_count     integer := 0;
  v_hold_mins integer := 15;
BEGIN
  FOR v_booking IN
    SELECT id
    FROM   public.bookings
    WHERE  status     = 'pending_payment'::public.app_booking_status
      AND  created_at < now() - (v_hold_mins || ' minutes')::interval
    LIMIT  100           -- batch: avoid long-lived transactions
  LOOP
    -- Release availability (idempotent if already released)
    PERFORM public.release_availability(v_booking.id);

    -- Cancel the booking only if it is still pending_payment.
    -- If a concurrent webhook has since advanced it, this UPDATE
    -- affects 0 rows and we leave it alone.
    UPDATE public.bookings
    SET    status               = 'cancelled_by_system'::public.app_booking_status,
           cancellation_reason  = 'payment_hold_expired',
           cancelled_at         = now(),
           updated_at           = now()
    WHERE  id     = v_booking.id
      AND  status = 'pending_payment'::public.app_booking_status;

    IF FOUND THEN
      INSERT INTO public.booking_events (
        booking_id, event_type,
        from_status, to_status,
        actor_id, reason, created_at
      ) VALUES (
        v_booking.id,
        'cancelled_by_system'::public.app_booking_event_type,
        'pending_payment'::public.app_booking_status,
        'cancelled_by_system'::public.app_booking_status,
        NULL,
        'payment_hold_expired',
        now()
      );

      v_count := v_count + 1;
    END IF;
  END LOOP;

  RETURN v_count;
END;
$$;

-- Only callable by server-side cron handler (service_role).
-- No GRANT to authenticated or anon.
GRANT EXECUTE ON FUNCTION public.expire_pending_bookings()
  TO service_role;


-- ============================================================
-- 9. TRIGGER: enforce booking state machine transitions
-- ============================================================
-- A BEFORE UPDATE trigger that rejects any status transition not in
-- the approved state machine.  This is the DB-layer backstop; the
-- server functions enforce business rules; this trigger enforces the
-- structural shape of allowed moves.
--
-- Invalid transitions raise SQLSTATE '23514' (check_violation)
-- with a descriptive message.

CREATE OR REPLACE FUNCTION public.validate_booking_transition()
  RETURNS trigger
  LANGUAGE plpgsql
  SET search_path = ''
AS $$
BEGIN
  -- No-op if status is not changing
  IF NEW.status = OLD.status THEN
    RETURN NEW;
  END IF;

  -- Allowed transition map
  IF NOT (
    -- From pending_payment
    (OLD.status = 'pending_payment'          AND NEW.status IN ('payment_processing', 'cancelled_by_system'))
    -- From payment_processing
    OR (OLD.status = 'payment_processing'    AND NEW.status IN ('awaiting_host', 'confirmed', 'cancelled_by_system'))
    -- From awaiting_host
    OR (OLD.status = 'awaiting_host'         AND NEW.status IN ('confirmed', 'cancelled_by_host', 'cancelled_by_system'))
    -- From confirmed
    OR (OLD.status = 'confirmed'             AND NEW.status IN ('checked_in', 'cancelled_by_traveler', 'cancelled_by_host', 'cancelled_by_system'))
    -- From checked_in
    OR (OLD.status = 'checked_in'            AND NEW.status IN ('completed', 'no_show', 'cancelled_by_system'))
    -- From completed (only dispute window)
    OR (OLD.status = 'completed'             AND NEW.status = 'disputed')
    -- From disputed (resolution)
    OR (OLD.status = 'disputed'              AND NEW.status IN ('completed', 'cancelled_by_system'))
  ) THEN
    RAISE EXCEPTION
      'Invalid booking status transition: % → % (booking_id: %)',
      OLD.status, NEW.status, OLD.id
      USING ERRCODE = 'check_violation';
  END IF;

  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_booking_state_machine
  BEFORE UPDATE OF status ON public.bookings
  FOR EACH ROW
  EXECUTE FUNCTION public.validate_booking_transition();


